    private void btnAccederActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAccederActionPerformed
        // TODO add your handling code here:
        user = txtUser.getText().trim();
        password = txtPassword.getText().trim();
        
        if (!user.equals("") || !password.equals("")) {
            try {
                Connection conn = Conexion.conectar();
                PreparedStatement pst = conn.prepareStatement(
                        "SELECT TIPO_NIVEL, ESTATUS, USERNAME, PASSWORD FROM USUARIOS WHERE USERNAME = '" + user + "'"
                                + " AND PASSWORD = '" + password + "'"
                );
                
                ResultSet rs = pst.executeQuery();
                
                if (rs.next()) {
                  String tipoNivel = rs.getString("TIPO_NIVEL");
                  String estatus = rs.getString("ESTATUS");
                  
                    if (tipoNivel.equalsIgnoreCase("Administrador") && estatus.equalsIgnoreCase("Activo")) {
                        dispose();
                        new Administrador().setVisible(true);
                    } else if (tipoNivel.equalsIgnoreCase("Capturista") && estatus.equalsIgnoreCase("Activo")) {
                        dispose();
                        new Capturista().setVisible(true);
                    } else if (tipoNivel.equalsIgnoreCase("Tecnico") && estatus.equalsIgnoreCase("Activo")) {
                        dispose();
                        new Tecnico().setVisible(true);
                    }
                } else  {
                    JOptionPane.showMessageDialog(null, "Datos de acceso incorrectos");
                    txtUser.setText("");
                    txtPassword.setText("");
                }
            } catch(SQLException e) {
                System.out.println("Error en el boton acceder: " + e.getMessage());
                JOptionPane.showMessageDialog(null, "Error al inicier sesi√≥n, contacte al administrador");
            }
        } else {
            JOptionPane.showMessageDialog(null, "Debes llenar todos los campos");
        }
    }//GEN-LAST:event_btnAccederActionPerformed

