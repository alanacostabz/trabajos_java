    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String text = txtCategoria.getText().trim();
        if (text.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Campo de la categoría vacío, ingrese un nombre.");
            } else {
              
            try {
                if (validacionDatos(text) > 0) {
                    JOptionPane.showMessageDialog(this, "La categoría " + text.toUpperCase() + " ya se encuentra en la base de datos.");
                    txtCategoria.setText("");
                } else {
                    int input = JOptionPane.showConfirmDialog(rootPane, "¿Está seguro de agregar la categoría " + text.toUpperCase() + "?","Agregar categoría", 0);
                    if (input == 0) {
                        StringBuilder sql
                                = new StringBuilder("INSERT INTO blood_banks.staff_categories (staff_category_description) VALUES (\'");
                        sql.append(text.toUpperCase());
                        sql.append("');");
                        System.out.println(sql.toString());
                        db.update(sql.toString());
                        
                        JOptionPane.showMessageDialog(this, "La categoría " + text.toUpperCase() + " se ha agregado correctamente.");
                        // doClose(returnStatus);
                        txtCategoria.setText("");
                        
                        
                    }
                }
            } catch (SQLException ex) {
                Logger.getLogger(insertCategory.class.getName()).log(Level.SEVERE, null, ex);
            }
            
        }
    }//GEN-LAST:event_jButton1ActionPerformed

