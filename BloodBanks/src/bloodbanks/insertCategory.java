        public int validacionDatos(String vd) throws SQLException {
        StringBuilder sql1 = new StringBuilder("select staff_category_description from blood_banks.staff_categories");
        int x = 0;
        try {
            ResultSet rs = db.query(sql1.toString());
            while (rs.next()) {

                String name = rs.getString(1);
                if (name.equalsIgnoreCase(vd)) {
                    x++;
                }

            }
        } catch (SQLException ex) {
            System.out.println(ex.getMessage());
        }
        return x;
    }
        
