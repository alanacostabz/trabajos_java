    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String id = textID.getText().trim();
        String aux = jnombre.getText();
        //textID.setText("");

        if (!id.isEmpty()) {
            int input = JOptionPane.showConfirmDialog(rootPane, "¿Está seguro de añadir el vino " + aux.toUpperCase() +" con ID = "+id+ "?", "Eliminar vino", 0);
            if (input == 0) {
                String sql = String.format("delete from winestore.wine_store where wine_store_id =%s", id);

                try {

                    int value = db.update(sql);
                    if (value == 0) {
                        JOptionPane.showMessageDialog(rootPane, "El vino no existe actualmente ó ya ha sido borrado antes.");
                    } else {
                        System.out.println(sql);
                        JOptionPane.showMessageDialog(rootPane, "El vino con id=" + id + " ha sido borrado exitosamente.");
                        textID.setText("");
                        jnombre.setText("");
                        jprecio.setText("");
                        jdescripcion.setText("");
                        jurl.setText("");
                        jcolor.setText("");
                        jpais.setText("");
                        jregion.setText("");
                        juva.setText("");
                        jviñedo.setText("");
                        jaño.setText("");
                        jfecha.setText(null);
//doClose(RET_OK);
                    }
                } catch (SQLException ex) {
                    System.out.println(ex.getMessage());
                }
            }
        } else {
            JOptionPane.showMessageDialog(rootPane, "Campo vacío, ingrese ID para eliminar.");
        }

    }//GEN-LAST:event_jButton1ActionPerformed

