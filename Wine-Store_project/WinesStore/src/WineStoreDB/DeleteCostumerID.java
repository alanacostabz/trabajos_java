    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String id = txtID.getText().trim();
        String combo = jnombre.getText();

        if (!id.isEmpty()) {

            int input = JOptionPane.showConfirmDialog(rootPane, "¿Está seguro de eliminar a el cliente " + combo + " con ID = " + id + "?", "Borrar cliente", 0);
            if (input == 0) {
                String sql = String.format("delete from winestore.customer where customer_id =%s", id);

                try {

                    int value = db.update(sql);

                    if (value == 0) {
                        JOptionPane.showMessageDialog(rootPane, "El cliente no existe ó ya ha sido eliminado antes.");
                    } else {
                        System.out.println(sql);
                        JOptionPane.showMessageDialog(rootPane, "El cliente con id=" + id + " ha sido borrado exitosamente.");
                        txtID.setText("");
                        jnombre.setText("");
                        jfecha.setText("");
                        jtelefono.setText("");
                        jdireccion.setText("");
                        jemail.setText("");
                        jtelefono.setText("");
                        jdireccion.setText("");
                        jpais.setText("");
// doClose(RET_OK);
                    }
                } catch (SQLException ex) {
                    System.out.println(ex.getMessage());
                }
            }
        } else {
                    jnombre.setText("");
                    jfecha.setText("");
                    jtelefono.setText("");
                    jdireccion.setText("");
                    jemail.setText("");
                    jtelefono.setText("");
                    jdireccion.setText("");
                    jpais.setText("");
            JOptionPane.showMessageDialog(rootPane, "Campo vacío, ingrese ID para eliminar.");
        }


    }//GEN-LAST:event_jButton1ActionPerformed

    private void txtIDKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtIDKeyTyped
        // TODO add your handling code here:
        int limit = 7;
        char c = evt.getKeyChar();
        if ((c < '0' || c > '9') && c != KeyEvent.VK_BACK_SPACE) {
            evt.consume();
            JOptionPane.showMessageDialog(rootPane, "Solo se pueden ingresar números.");
        }
        if (txtID.getText().length() == limit) {
            evt.consume();
            getToolkit().beep();
        }
    }//GEN-LAST:event_txtIDKeyTyped

