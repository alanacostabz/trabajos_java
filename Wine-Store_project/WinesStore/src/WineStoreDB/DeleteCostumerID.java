    private void txtIDKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtIDKeyReleased
        // TODO add your handling code here:
        String idc = txtID.getText().toString();
        jLabel3.setText("");

        if (!idc.equals("")) {
            try {
                if (validacionDatosC(idc) != 0) {

                    jLabel3.setText("ID valido.");
                    final String sql1 = String.format("select nombre, apellido, fecha_nacimiento, country_name, email, telefono, direccion from winestore.customer inner join winestore.ref_countries\n"
                            + "on winestore.customer.country_code = winestore.ref_countries.country_code where customer_id = " + idc);
                    try {

                        ResultSet rs = db.query(sql1);

                        while (rs.next()) {
                            //comboRegion.addItem("NULL");
                            nombre = rs.getString(1);
                            apellido = rs.getString(2);
                            fecharu = rs.getDate(3);
                            pais = rs.getString(4);
                            email = rs.getString(5);
                            telefono = rs.getString(6);
                            direccion = rs.getString(7);

                        }
                    } catch (SQLException ex) {
                        System.out.println(ex.getMessage());
                    }

                    jnombre.setText(apellido + " " + nombre);
                    jfecha.setText(fecharu.toString());
                    jtelefono.setText(telefono);
                    jdireccion.setText(direccion);
                    jemail.setText(email);
                    jtelefono.setText(telefono);
                    jdireccion.setText(direccion);
                    jpais.setText(pais);

                } else {
                    jLabel3.setText("ID no existente.");
                    jnombre.setText("");
                    jfecha.setText("");
                    jtelefono.setText("");
                    jdireccion.setText("");
                    jemail.setText("");
                    jtelefono.setText("");
                    jdireccion.setText("");
                    jpais.setText("");
                }
            } catch (SQLException ex) {
                Logger.getLogger(DeletePurchase.class.getName()).log(Level.SEVERE, null, ex);
            }
        }


    }//GEN-LAST:event_txtIDKeyReleased

