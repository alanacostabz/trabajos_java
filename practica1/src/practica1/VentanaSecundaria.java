    private void btnAplicarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAplicarActionPerformed
        // TODO add your handling code here:
        String sexo = "";
        if (cbHembra.isSelected()){
            sexo = cbHembra.getText();
            
        } else if (cbMacho.isSelected()) {
            sexo = cbMacho.getText();
        }
        
        int validar = Integer.parseInt(txtEdad.getText());
        
        if (validar <= 0 || validar >= 20) {
            JOptionPane.showMessageDialog(null,"Edad no valida!!");
        } else {
        
        miPerro.nombre = txtNombre.getText();
        miPerro.raza = jlRaza.getSelectedValue();
        miPerro.edad = Integer.parseInt(txtEdad.getText());
        miPerro.tamaño = cbTamano.getSelectedItem().toString();
        miPerro.sexo = sexo;
        miPerro.vacunado = cbVacunado.isSelected();
        this.dispose();
        }
    }//GEN-LAST:event_btnAplicarActionPerformed

