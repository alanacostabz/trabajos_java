    private void txtPromedioKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtPromedioKeyPressed

        int tecla = evt.getKeyCode();
        if ((tecla >= evt.VK_0 && tecla <= evt.VK_9)
                || (tecla >= evt.VK_NUMPAD0 && tecla <= evt.VK_NUMPAD9)
                || (tecla == evt.VK_BACK_SPACE) || (tecla == evt.VK_PERIOD)) {
            if (txtPromedio.getText().matches(".")) {
                return;
            }
            txtPromedio.setEditable(true);
        } else {
            txtPromedio.setEditable(false);
        }

    }//GEN-LAST:event_txtPromedioKeyPressed

