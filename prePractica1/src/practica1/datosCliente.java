    private void btnGrabarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGrabarActionPerformed
        // TODO add your handling code here:
        String sexo = "";
        if (rbMujer.isSelected()){
            sexo = rbMujer.getText();
            
        } else if (rbHombre.isSelected()) {
            sexo = rbHombre.getText();
        }
        try{
            
        this.clienteX.nombre=txtNombre.getText();
        this.clienteX.apellidos = txtApellidos.getText();
        this.clienteX.sexo=sexo;
        this.clienteX.fechaNacimiento = new SimpleDateFormat("dd/MM/yyy").parse(txtFechaNacimiento.getText());
        this.clienteX.activo=cbActivo.isSelected();
        this.clienteX.celular = txtCelular.getText();
        
        this.dispose();
            
        } catch (Exception e){
                JOptionPane.showMessageDialog(null, "ExcepciÃ³n: " + e.toString());
                return;
        }
        
    }//GEN-LAST:event_btnGrabarActionPerformed

